void OLED_Init(void)
{
	uint8_t init[] = {
	      0x00,
	      0xAE, // display OFF
	      0x20, 0x00, // horizontal addressing mode
	      0xB0,
	      0xC8,
	      0x00,
	      0x10,
	      0x40,
	      0x81, 0x7F,
	      0xA1,
	      0xA6,
	      0xA8, 0x3F,
	      0xA4,
	      0xD3, 0x00,
	      0xD5, 0x80,
	      0xD9, 0xF1,
	      0xDA, 0x12,
	      0xDB, 0x40,
	      0x8D, 0x14,
	      0xAF // display ON
	  };
	  HAL_I2C_Master_Transmit(&hi2c1, OLED_ADDR, init, 29, TIMEOUT);
}

void OLED_Clear(void)
{
	memset(buffer, 0, sizeof(buffer));
}

void OLED_DrawPixel(uint8_t x, uint8_t y, uint8_t color)
{
	if (x >= 128 || y >= 64) return;

	uint16_t index = x + (y / 8) * 128;
	uint8_t bit = 1 << (y % 8);

	if(color)
	{
		buffer[index] |= bit;
	}
	else
	{
		buffer[index] &= ~bit;
	}
}

void OLED_DrawChar(uint8_t x, uint8_t page, const char c)
{
	if (c < 32 || c > 127)
		return;

	const uint8_t *glyph = font5x7[c - 32];

	uint16_t base = x + (page * 128);

	for (uint8_t i = 0; i < 5; i++)
	{
	    if (x + i >= 128)
	    	break;
	    buffer[base + i] = glyph[i];
	}

	// spacing
	if (x + 5 < 128)
	{
	    buffer[base + 5] = 0x00;
	}
}

void OLED_Print(uint8_t x, uint8_t page, const char *str)
{
	if (page >= 8) return;

	while (*str)
	{
		if (x > 122) break;

		OLED_DrawChar(x, page, *str++);
		x += 6;
	}
}

