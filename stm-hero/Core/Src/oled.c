const char* IntToString(uint16_t number)
{
	static char buffer[10];
	snprintf(buffer, sizeof(buffer), "%d", number);
	return buffer;
}

static void OLED_WriteCommand(uint8_t cmd)
{
    uint8_t data[2];
    data[0] = 0x00;
    data[1] = cmd;

    HAL_I2C_Master_Transmit(&hi2c1, OLED_ADDR, data, 2, TIMEOUT);
}

